<h2>Welcome to APRS-Alert.</h2><br>
APRS-Alert is a notification system that ties the RF world of <a href="http://www.aprs.org">APRS</a>
to real-time notification via SMS messaging and email.<br>
<br>
APRS-Alert works by constantly monitoring the real-time stream of position updates from APRS-IS and
compares each station heard to a list of stations that users are interested in.  If an "interesting"
station sends a position update, the system scans through a list of rules and sends notifications via
email to the addresses of people interested in the movement of those stations.<br>
<p>
To get started, please <a href="register.php">register</a> with the system and begin
setting up monitored stations, zones of interest, notification time windows and addresses, and 
rules to tie all these elements together.</p><br>
Here are some <a href="examples.php">examples</a> to whet your appetite.<br>
